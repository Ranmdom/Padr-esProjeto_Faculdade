package Usuario;

public class Usuario {
    private String nome;
    private String email;
    private int idade;
    private String endereco;
    private String telefone;


    public Usuario(String nome, String email, int idade, String endereco, String telefone) {
        this.nome = nome;
        this.email = email;
        this.idade = idade;
        this.endereco = endereco;
        this.telefone = telefone;
    }
}
